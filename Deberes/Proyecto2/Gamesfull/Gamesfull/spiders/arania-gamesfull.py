class AraniaMangadoorEnlaces(CrawlSpider):
    name = 'enlaces'  # Heredado (override)

    allowed_domains = [
        'gamesfull.org'
    ]

    start_urls = [
        'https://www.gamesfull.org/category/accion/page/1/',
        'https://www.gamesfull.org/category/accion/page/14/'
    ]

    # Busca todo
    regla_uno = (
        Rule(LinkExtractor(), callback='parse_page'),
    )

    url_segmento_permitido = (
        'accion/page/'
    )

    # Busca dentro de los dominios permitidos y los segmentos permitidos
    regla_dos = (
        Rule(
            LinkExtractor(
                allow_domains=allowed_domains,
                allow=url_segmento_permitido
            ), callback='parse_page'
        ),
    )

    rules = regla_dos  # Heredado (override)

    # Obtener tÃ­tulos del manga
    def parse_page(self, response):
        lista_enlaces = response.css(
            'article.TPost.B > a::attr(href)').extract()
        for enlace in lista_enlaces:
            with open('../juegosEnlaces.txt', 'a+', encoding='utf-8') as archivo:  # se evita el try catch
                archivo.write('\'' + enlace + '\'' + ',\n')

